public class Sanse {
    private Integer idSanse;
    private Integer vrijednost;
    private String opis;

    public Sanse() {
    }

    public Integer getIdSanse() {
        return idSanse;
    }

    public void setIdSanse(Integer idSanse) {
        this.idSanse = idSanse;
    }

    public Integer getVrijednost() {
        return vrijednost;
    }

    public void setVrijednost(Integer vrijednost) {
        this.vrijednost = vrijednost;
    }

    public String getOpis() {
        return opis;
    }

    public void setOpis(String opis) {
        this.opis = opis;
    }
    
    
}
